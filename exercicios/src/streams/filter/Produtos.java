package streams.filter;

public class Produtos {
    
    final String nome;
    final double preco;
    final double desconto;
    final double frete;
	
	public Produtos(String nome, double preco, double desconto, double frete) {
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.frete = frete;
	}
	

}
